package Errors;

/**
 * Exception thrown when a nonexistent field is acted on.
 */
@SuppressWarnings("serial")
public class NullFieldException extends TableException {

	/**
	 * Creates message explaining the error and field that failed instantiation.
	 */
	public NullFieldException(String field) {
		super("Field \"" + field + "\" does not exist.");
	}
}
