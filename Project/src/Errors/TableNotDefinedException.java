package Errors;

/**
 * Exception thrown when an uninstantiated table is acted on.
 */
@SuppressWarnings("serial")
public class TableNotDefinedException extends TableException {

	/**
	 * Creates message explaining the error and uninstantiated table.
	 */
	public TableNotDefinedException(String table) {
		super("Table \"" + table + "\" has not been instantiated.");
	}
}